#include "guid.h"
#include "alloc.h"
#include "ops.h"

rf_object_t rf_guid_generate()
{
    i64_t i;
    rf_object_t g = guid(NULL);
    g.guid = (guid_t *)rf_malloc(sizeof(guid_t));

    for (i = 0; i < 16; i++)
        g.guid->data[i] = rand_i64() % 256;

    return g;
}